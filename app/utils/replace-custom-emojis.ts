const plainSlackEmojis = Object.keys(emojisFromSlack).reduce((acc, emoji) => {
  const cleaned = emoji.split("-").slice(0, -1).join("-");
  acc[cleaned] = emojisFromSlack[emoji];
  return acc;
}, {} as { [key: string]: { image: string } });

export function isCustomEmoji(emoji: string) {
  const lowercase = emoji.toLowerCase();
  return !!customEmojis[lowercase] || !!emojisFromSlack[lowercase] || plainSlackEmojis[lowercase];
}

export default function replaceCustomEmojis(
  markdown: string,
  cdnHost: string = ""
) {
  const emojiRegex = /:(.*?):/g;
  return markdown.replace(emojiRegex, (match, emoji) => {
    const customEmoji = customEmojis[emoji] || emojisFromSlack[emoji] || plainSlackEmojis[emoji];
    if (customEmoji) {
      return `![${emoji}](${cdnHost}/custom-emojis/${customEmoji.image})`;
    }
    return match;
  });
}

export function getCustomEmojiURL(
  emoji: string,
  cdnHost: string = ""
): string | null {
  const lowercased = emoji.toLowerCase();
  const customEmoji = customEmojis[lowercased] || emojisFromSlack[lowercased] || plainSlackEmojis[lowercased];
  if (customEmoji) {
    return `${cdnHost}/custom-emojis/${customEmoji.image}`;
  }
  return null;
}
